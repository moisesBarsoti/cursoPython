# Igualdade ==
n1 = 10
n2 = 10
print(n1 == n2)

# Diferente !=
n3 = 100
n4 = 101
print(n3 != n4)

# Maior que > ou maior que ou igual >=
age = 18
entrada = 18
print(age > entrada) # Somente verifca se é maior
print(age >= entrada) # Aqui ele verifca se é maior ou igual

# Menor que < ou menor que ou igual <=
age = 18
entrada = 18
print(age < entrada) # Somente verifca se é menor
print(age <= entrada) # Aqui ele verifca se é menor ou igual