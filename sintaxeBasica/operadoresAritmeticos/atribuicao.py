# Adição
saldo = 200
saldo += 200
print(saldo) # 400

# Subtração
saldo2 = 200
saldo2 -= 200
print(saldo2) # 0

# Divisão
saldo3 = 200
saldo3 /= 200
print(saldo3) # 1

# Multiplicação
saldo4 = 20
saldo4 *= 20
print(saldo4) # 400

# Exponenciação
saldo5 = 20
saldo5 **= 2
print(saldo5) # 400

# Resto da divisão
saldo6 = 200
saldo6 %= 200
print(saldo6) # 0