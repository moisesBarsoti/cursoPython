# Regras da matemática

# Primeiro se resolve o que está nos parenteses por exemplo
print((8 + 6) / 2) # 7

# Após os parenteses se resolve primeiro a divisão - multiplicação - subtração - adição

# Adição
print(1 + 1) # 2

# Subtração
print(1 - 1) # 0

# divisão
print(1 / 1) # 1

# Multiplicação
print(1 * 1) # 1

# Outros exemplos

# Ao quadrado
print(10 ** 2 / 2) # 50
print(10 ** 2 + (2 + 2)) # 104